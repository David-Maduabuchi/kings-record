// this function generates random DATA
const generateData = (count: number, range: { min: number; max: number }) => {
  const data = [];
  for (let i = 0; i < count; i++) {
    const x = i;
    const y = Math.floor(
      Math.random() * (range.max - range.min + 1) + range.min
    );
    data.push({ x, y });
  }
  return data;
};

